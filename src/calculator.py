class NumCalculator:

    def plus(self, x, y):
        return x + y
    def minus(self, x , y):
        return x - y
    def mult(self, x, y):
        return x * y
    def div(self, x, y):
        return x/y
    def full_div(self, x, y):
        return x // y
    def end_div(self, x, y):
        return x % y